package com.beans;

public class ShoppingCar {
	public int getShopcarid() {
		return shopcarid;
	}
	public void setShopcarid(int shopcarid) {
		this.shopcarid = shopcarid;
	}
	public String getShopcarname() {
		return shopcarname;
	}
	public void setShopcarname(String shopcarname) {
		this.shopcarname = shopcarname;
	}
	public int getShopcarprice() {
		return shopcarprice;
	}
	public void setShopcarprice(int shopcarprice) {
		this.shopcarprice = shopcarprice;
	}
	public String getShopcarimgpath() {
		return shopcarimgpath;
	}
	public void setShopcarimgpath(String shopcarimgpath) {
		this.shopcarimgpath = shopcarimgpath;
	}
	public int getSign() {
		return sign;
	}
	public void setSign(int sign) {
		this.sign = sign;
	}
	private int shopcarid;
	private String shopcarname;
	private int shopcarprice;
	private String shopcarimgpath;
	private int sign;
	

}
