package com.beans;

public class Equipment {
	private int epid;
	private String epname;
	private int epprice;
	private String epimgpath;
	private int sign;
	
	public String getEpname() {
		return epname;
	}
	public void setEpname(String epname) {
		this.epname = epname;
	}

	public int getEpprice() {
		return epprice;
	}
	public void setEpprice(int epprice) {
		this.epprice = epprice;
	}

	public String getEpimgpath() {
		return epimgpath;
	}
	public void setEpimgpath(String epimgpath) {
		this.epimgpath = epimgpath;
	}
	public int getSign() {
		return sign;
	}
	public void setSign(int sign) {
		this.sign = sign;
	}
	
	public int getEpid() {
		return epid;
	}
	public void setEpid(int epid) {
		this.epid = epid;
	}
	
	


}
