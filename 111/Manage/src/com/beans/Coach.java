package com.beans;

public class Coach {
	private int coachid;
	private String coachname;
	private int coachprice;
	private String coachimgpath;
	private int sign;
	public int getCoachprice() {
		return coachprice;
	}
	public void setCoachprice(int coachprice) {
		this.coachprice = coachprice;
	}
	
	

	public String getCoachimgpath() {
		return coachimgpath;
	}
	public void setCoachimgpath(String coachimgpath) {
		this.coachimgpath = coachimgpath;
	}
	public int getSign() {
		return sign;
	}
	public void setSign(int sign) {
		this.sign = sign;
	}
	
	public int getCoachid() {
		return coachid;
	}
	public void setCoachid(int coachid) {
		this.coachid = coachid;
	}
	public String getCoachname() {
		return coachname;
	}
	public void setCoachname(String coachname) {
		this.coachname = coachname;
	}
	

}
